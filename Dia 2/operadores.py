x = 6
y = 2
z = 7

print(f"{x} + {y} = {x+y}")# forma de una suma con formate de cadenas
print(f"{x} mas  {y} es igual a  {x+y}")# forma mostrando mas detalles en el print
print(f"{x} menos  {y} es igual a  {x-y}")
print(f"{x} por  {y} es igual a  {x*y}")
print(f"{x} dividido  {y} es igual a  {x/y}")

print(f"{z} dividido al piso de {y} es igual a  {z//y}") #divicion al piso cuando el resultado tien numero devimal se elimina pra dejar solo el numero entero ejem. 7//2 =3
print(f"{z} modulo de {y} es igual a  {z%y}")# modulo, lo que sobra de una divicion exacta eiem. 7%2 = 1

print(f"{x} elevado  {y} es igual a  {x**y}")# elevado a al cuadrado
print(f"{x} elevado al  {3} es igual a  {x**3}")# elevado a al cubo
print(f"la raiz cuadrada de {x} es igual a  {x**0.5}")# sacar la raiz cuadrada